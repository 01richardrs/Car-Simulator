public class Road {
    private int length;
    private int roadnum;
    private int height;

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public int getRoadnum() {
        return roadnum;
    }

    public void setRoadnum(int roadnum) {
        this.roadnum = roadnum;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
